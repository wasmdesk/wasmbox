// fallbackGlyph is rendered for any byte outside the font table -- a solid
// block keeps "unknown glyph" visually obvious.
var fallbackGlyph = [8]byte{0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF}

// Glyph returns the 8x8 bitmap for c. Bytes outside the table fall back to
// the solid block so unknown bytes still produce visible output.
func Glyph(c byte) [8]byte {
	if g, ok := font[c]; ok {
		return g
	}
	return fallbackGlyph
}
