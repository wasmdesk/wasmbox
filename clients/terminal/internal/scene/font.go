// Glyph returns the 8x8 bitmap for c. ASCII printables 0x20..0x7E come from
// the table; anything else falls back to 0x7F (the solid block) so unknown
// bytes still produce visible output instead of vanishing silently.
func Glyph(c byte) [8]byte {
	if g, ok := font[c]; ok {
		return g
	}
	return font[0x7F]
}
